int	ft_strcmp(const char *s1, const char *s2)
{
	while (*((unsigned char*)s1) == *((unsigned char*)s2++) && *s1 != 0)
		s1++;
	return (*((unsigned char*)s1) - *((unsigned char*)s2 - 1));
}
