#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	c;

	c = '\n';
	i = 0;
	while (s[i])
		i++;
	write(fd, s, i);
	write(fd, &c, 1);
}
